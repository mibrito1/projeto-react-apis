

export const switchCores = (type) => {

    switch (type) {

        case "grass":
            return "#729F92"

        case "normal":
            return "#BF9762"

        case "bug":
            return "#76A866"

        case "fire":
            return "#EAAB7D"

        case "water":
            return "#71C3FF"

    }

}